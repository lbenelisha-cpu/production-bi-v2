-- ADAMA Production BI - Supabase Schema
-- Run this in Supabase SQL Editor.

create table if not exists facilities (
  id text primary key,
  name text not null,
  unit text default 'ליטר',
  target_type text default 'daily',
  production_target numeric default 0,
  packaging_target numeric default 0,
  active boolean default true,
  updated_at timestamptz default now()
);

create table if not exists packaging_lines (
  id text primary key,
  name text not null,
  daily_target numeric default 0,
  unit text default 'ליטר',
  active boolean default true,
  updated_at timestamptz default now()
);

create table if not exists production_entries (
  id text primary key,
  date date,
  facility text,
  packaging_line text,
  shift text,
  production numeric default 0,
  packaging numeric default 0,
  order_no text,
  batch text,
  note text,
  source text,
  created_at timestamptz default now()
);

create table if not exists quality_entries (
  id text primary key,
  date date,
  facility text,
  order_no text,
  batch text,
  material text,
  inspection_lot text,
  status text,
  source text,
  created_at timestamptz default now()
);

create table if not exists app_audit_log (
  id bigint generated by default as identity primary key,
  at timestamptz default now(),
  action text,
  entity text,
  details jsonb
);


-- Simple demo policy for early testing only:
-- In production, enable authentication and replace these policies.
alter table facilities enable row level security;
alter table packaging_lines enable row level security;
alter table production_entries enable row level security;
alter table quality_entries enable row level security;
alter table app_audit_log enable row level security;

drop policy if exists "public read facilities" on facilities;
create policy "public read facilities" on facilities for select using (true);
drop policy if exists "public write facilities" on facilities;
create policy "public write facilities" on facilities for all using (true) with check (true);

